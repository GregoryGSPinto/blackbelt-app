export async function mockGetAcademyHealth(_academyId: string): Promise<AcademyHealthSummary> {
  await delay();
  const scores = MOCK_SCORES;
  const total = scores.length;
  const avg = Math.round(scores.reduce((s, sc) => s + sc.score, 0) / total);
  const critical = scores.filter((s) => s.risk === 'critical').length;
  const atRisk = scores.filter((s) => s.risk === 'high' || s.risk === 'medium').length;
  const healthy = scores.filter((s) => s.risk === 'low').length;
  return { average_score: avg, total_students: total, at_risk: atRisk, critical, healthy };
}
